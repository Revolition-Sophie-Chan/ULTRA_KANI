#include <time.h>
#include"Tutorial0.h"
#include"fade.h"
#include"input.h"
#include"text.h"
#include"sound.h"

//=============================================================================
// グローバル変数
//=============================================================================
int g_nCounterTutorial0state = 0;
TUTORIAL0STATE g_Tutorial0State = TUTORIAL0STATE_NONE;
//PLAYER *pPlayer;
//=============================================================================
// 初期化処理
//=============================================================================
void InitTutorial0(void)
{	
	InitMoji();
	SetText(D3DXVECTOR3(640.0f, 360.0f, 000.0f), 1280 / 2, 720 / 2, 7);

	SetText(D3DXVECTOR3(630.0f, 680.0f, 000.0f), 130, 30, 0);
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitTutorial0(void)
{
	UninitMoji();
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateTutorial0(void)
{
	UpdateMoji();

	if (GetKeyboardTrigger(DIK_P)|| GetKeyboardTrigger(DIK_RETURN))
	{
		g_Tutorial0State = TUTORIAL0STATE_NONE;
		SetFade(MODE_TUTORIAL);
	}

}
//=============================================================================
// びょうが処理
//=============================================================================
void DrawTutorial0(void)
{
	DrawMoji();

}
//=============================================================================
// 設定
//=============================================================================
void SetTutorial0State(TUTORIAL0STATE state)
{
	g_Tutorial0State = state;
	g_nCounterTutorial0state = 0;
}
//=============================================================================
// ゲーム状態の取得
//=============================================================================
TUTORIAL0STATE GetTutorial0State(void)
{
	return g_Tutorial0State;
}